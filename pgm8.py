# "**" exponent/power
#a**b= a power of b
#2**3=8

#int**int->int
a=2
b=3
print(a**b)

#int**float->float
a=20
b=10.5
print(a**b)

#int**complex->complex
a=20
b=2+9j
print(a**b)

#float**complex->complex
a=10.5
b=30+9j
print(a**b)

#complex*complex->complex
a=10+9j
b=20+8j
print(a**b)

