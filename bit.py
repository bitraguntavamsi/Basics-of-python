# Bitwise "&" (and)

# True->1
#False->0

print(16&8)
print(10&12)

#Bitwise "|" (or)

print(10|12)
print(1|2)

# Bitwise "^" (xor)

print(32^16)
print(10^12)

# Bitwise "<<" (left shift)

print(10<<2)
print(20<<3)

# Bitwise ">>" (Right shift)

# we should add only "0" on the MSB side...
# if the value is in "-" minus then we add 1 on the MSB side...

print(12>>3)
print(13>>3)

# Bitwise "~" (tildle)

# if it is postive integer increase "1" and add negative "-"...
# if it is negative integer decrease "1" and add postive "+"...

print(~105)
print(~77)
print(~-100)
print(~-200)










